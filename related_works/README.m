#The main features of this work:
First research prototype applying **generative LLM** for generating of AAS
Generative LLM (inferencing) and embedding LLM (similarity search)

Knowledge stored in LLM instead of manually crafted mapping rules
Wider range of **input flexibility** and format independent

The system add proper **semantic annotation** for disambiguating the **concepts of the data property**
The system is able to optimize relevant details for **better quality** of AAS 

This prototype demonstrate the machine capability to **semantically understand and generate data properties**.
This capability can enable:
  1) an **interoperable** information exchange in context of **digitalization**
  2) and higher degree of **task automation** in context of **autonomization**

